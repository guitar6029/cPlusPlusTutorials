#include <iostream>

int main() {
    int age = 25;
    double pi = 3.14159;
    char grade = 'A';
    std::string name = "John Doe";
    bool isCodingFun = true;

    std::cout << "Age: " << age << std::endl;
    std::cout << "Pi: " << pi << std::endl;
    std::cout << "Grade: " << grade << std::endl;
    std::cout << "Name: " << name << std::endl;
    std::cout << "Is coding fun? " << isCodingFun << std::endl;

    return 0;

}